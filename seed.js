    // Xóa dữ liệu cũ
    const User = require('./models/User');
    const Teacher = require('./models/Teacher');
    const TeacherPosition = require('./models/TeacherPosition');

    await User.deleteMany({});
    await Teacher.deleteMany({});
    await TeacherPosition.deleteMany({});
